package java_basics;

// 'class' variables use Pascal-case by convention
public class Variables {

    public static void main(String[] args) {

        // VARIABLES - use camel-case.

        int x;  // variable declaration with a data-type
        x = 4;  // variable assignment

        System.out.println(x);  // print x

        x = 234;    // re-assigning variable

        System.out.println(x);  // print x
        System.out.println(x + 4);  // print x + 4

        String words = "this is a sentence";
        System.out.println(words + " with extra words added.");

    }

}
